const nav = document.querySelector("nav");

sections.forEach((section) => {
  const details = document.createElement("details");
  details.className = "p-8 mb-6 bg-white/60 rounded-lg shadow-sm mt-4 mb-6"; // Ajustado margen superior (mt-4) y margen inferior (mb-6)
  const summary = document.createElement("summary");
  summary.className = "text-xl font-semibold text-blue-800";
  summary.textContent = section.title;
  details.appendChild(summary);
  nav.appendChild(details); // Asegúrate de que details se añada correctamente al contenedor

  const mainContentDiv = document.createElement("div");
  mainContentDiv.className = "ml-4 mt-2";

  // Nuevo soporte para múltiples tipos de contenido
  if (section.content) {
    if (typeof section.content === "string") {
      mainContentDiv.innerHTML = section.content;
    } else if (Array.isArray(section.content)) {
      section.content.forEach((item) => {
        if (item.type === "subtitle") {
          const subtitle = document.createElement("h3");
          subtitle.className = "text-lg font-bold text-gray-700 mt-4";
          subtitle.textContent = item.content;
          mainContentDiv.appendChild(subtitle);
        } else if (item.type === "html") {
          const htmlBlock = document.createElement("div");
          htmlBlock.innerHTML = item.content;
          mainContentDiv.appendChild(htmlBlock);
        } else if (item.type === "image") {
          const imgBlock = document.createElement("div");
          imgBlock.className = "my-4 text-center";

          const img = document.createElement("img");
          img.src = item.src;
          img.alt = item.caption;
          img.className =
            "mx-auto rounded shadow-lg max-w-full h-auto object-top";

          const caption = document.createElement("p");
          caption.className = "text-sm text-gray-600 mt-1";
          caption.textContent = item.caption;

          if (item.link) {
            const imageLink = document.createElement("a");
            imageLink.href = item.link;
            imageLink.target = "_blank";
            imageLink.appendChild(img);
            imgBlock.appendChild(imageLink);
          } else {
            imgBlock.appendChild(img);
          }
          imgBlock.appendChild(caption);
          mainContentDiv.appendChild(imgBlock);
        } else if (item.type === "gallery") {
          const galleryTitle = document.createElement("h4");
          galleryTitle.className =
            "text-md font-semibold text-amber-700 mt-4 mb-2";
          galleryTitle.textContent = item.title || "Galería";
          mainContentDiv.appendChild(galleryTitle);

          const gallery = document.createElement("div");
          gallery.className = "grid grid-cols-2 md:grid-cols-3 gap-4";
          item.images.forEach((img, i) => {
            const imgCard = document.createElement("div");
            imgCard.className =
              "bg-white/60 rounded-lg shadow p-2 cursor-pointer transition-transform hover:scale-105";
            imgCard.innerHTML = `
              <img src="${img.src}" alt="${img.caption}" class="rounded w-full h-48 object-cover object-top"/>
              <p class="text-center mt-1 text-sm">${img.caption}</p>`;
            imgCard.addEventListener("click", () =>
              openCarousel(item.images, i)
            );
            gallery.appendChild(imgCard);
          });

          mainContentDiv.appendChild(gallery);
        }
      });
    }
    details.appendChild(mainContentDiv);
  }

  if (section.children) {
    section.children.forEach((child) => {
      const subDetails = document.createElement("details");
      subDetails.className = "ml-4 p-3 mb-4 bg-white/30 rounded mt-2"; // Ajuste con margen superior mt-2 para subdetails

      const subSummary = document.createElement("summary");
      subSummary.className = "text-lg text-amber-700 ml-4";
      subSummary.textContent = child.title;
      subDetails.appendChild(subSummary);

      const contentDiv = document.createElement("div");
      contentDiv.className = "ml-6 mt-2";

      if (child.type === "gallery") {
        const gallery = document.createElement("div");
        gallery.className = "grid grid-cols-2 md:grid-cols-3 gap-4 mb-6";
        child.images.forEach((img, i) => {
          const imgCard = document.createElement("div");
          imgCard.className =
            "bg-white/60 rounded-lg shadow p-2 cursor-pointer transition-transform hover:scale-105";
          imgCard.innerHTML = `
            <img src="${img.src}" alt="${img.caption}" class="rounded w-full h-48 object-cover object-top"/>
            <p class="text-center mt-1 text-sm">${img.caption}</p>`;
          imgCard.addEventListener("click", () =>
            openCarousel(child.images, i)
          );
          gallery.appendChild(imgCard);
        });
        contentDiv.appendChild(gallery);
      } else {
        contentDiv.innerHTML = child.content;
      }

      subDetails.appendChild(contentDiv);
      details.appendChild(subDetails);
    });
  }

  nav.appendChild(details);
});

// Carousel logic
let currentImages = [],
  currentIndex = 0;

function openCarousel(images, index) {
  currentImages = images;
  currentIndex = index;
  updateCarousel();

  const modal = document.getElementById("carouselModal");
  modal.classList.remove("hidden");
  modal.classList.add("flex"); // Asegura que se vea con display flex
}

function updateCarousel() {
  const img = currentImages[currentIndex];
  const imgElement = document.getElementById("carouselImage");

  imgElement.src = img.src;
  imgElement.alt = img.caption;
  imgElement.className =
    "w-full h-auto max-h-[80vh] object-contain rounded-lg shadow-lg mx-auto";

  document.getElementById("carouselCaption").textContent = img.caption;
}

document.getElementById("nextBtn").addEventListener("click", () => {
  currentIndex = (currentIndex + 1) % currentImages.length;
  updateCarousel();
});

document.getElementById("prevBtn").addEventListener("click", () => {
  currentIndex =
    (currentIndex - 1 + currentImages.length) % currentImages.length;
  updateCarousel();
});

document.getElementById("closeModal").addEventListener("click", () => {
  document.getElementById("carouselModal").classList.add("hidden");
});

window.addEventListener("scroll", () => {
  const heroText = document.getElementById("heroText");
  if (window.scrollY > 100) {
    heroText.classList.add("hero-hidden");
  } else {
    heroText.classList.remove("hero-hidden");
  }
});
